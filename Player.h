#pragma once

#include "Vec2.h"

class LoadScene;

class Player
{
public:
	Player();
	virtual ~Player();

	// グラフィックデータ設定
	void setHandle(int handle) { m_handle = handle; }

	// プレイヤーの初期化
	void init();

	// ScneneMainクラスのポインタ設定
	void setMain(LoadScene* pMain) { m_pMain = pMain; }
	
	// 処理
	void update();

	// 描画
	void draw();

	// 情報の取得
	Vec2 getPos() const { return m_pos; }

private:

	int m_handle;
	
	// SceneMainのポインタ
	LoadScene* m_pMain;
	
	// 表示位置
	Vec2 m_pos;

	// 移動
	Vec2 m_vec;

};
